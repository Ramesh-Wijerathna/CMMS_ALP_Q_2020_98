    private void btnReportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReportActionPerformed

        boolean result = false;

        try {

            if (txtIssueDetails.getText().trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please Enter Issue Details");
            } else if (ComboFlag.getSelectedIndex() == -1) {
                JOptionPane.showMessageDialog(this, "Please Select Flag");

            } else if (txtInventoryID.getText().trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please Enter Inventory ID");

            } else if (txtBranchID.getText().trim().isEmpty() || Integer.parseInt(txtBranchID.getText()) <= 0) {
                JOptionPane.showMessageDialog(this, "Please Enter Branch ID");

            } else {

                String MaintainceID = txtMaintainID.getText();
                int BranchID = Integer.parseInt(txtBranchID.getText());
                String Flag = ComboFlag.getSelectedItem().toString();
                String IssueDetails = txtIssueDetails.getText();
                String RecordDate = lblRecordDate.getText();
                String InventoryID = txtInventoryID.getText();

                if (Flag == "Major") {
                    Maintenance_Issue issue = new Maintenance_Issue(MaintainceID, BranchID, InventoryID, "0", " ", Flag, IssueDetails, RecordDate, "Reported", "Not Approved");
                    result = bsl.ReportIssue(issue);
                    RegionManagerBusinessLayer.assignRegionManager();

                } else {
                    Maintenance_Issue issue = new Maintenance_Issue(MaintainceID, BranchID, InventoryID, "0", " ", Flag, IssueDetails, RecordDate, "Reported", "Approved");
                    result = bsl.ReportIssue(issue);
                    
                    
                }

            }

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Please Enter Valid Inputs");
        }

        if (result) {
            JOptionPane.showMessageDialog(this, "Issue Reported");

            ComboFlag.setSelectedItem(-1);
            txtBranchID.setText("");
            txtIssueDetails.setText("");
            txtInventoryID.setText("");
            GenarateID();

        }


    }//GEN-LAST:event_btnReportActionPerformed

