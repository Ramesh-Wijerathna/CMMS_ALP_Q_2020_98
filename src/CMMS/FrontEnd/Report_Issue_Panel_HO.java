    // load all inventory to the table
    public void LoadInventory(int BrancID) {
        ArrayList<Inventory> eqps = EquipmentServiceLayer.GetAllEquipments(BrancID);

        DefaultTableModel tbl = (DefaultTableModel) InventoryTable.getModel();
        tbl.setRowCount(0);

        for (Inventory inv : eqps) {
            Object[] rowData = {
                inv.getInID(),
                inv.getItemName(),
                inv.getModel(),
                inv.getSelialNumber(),
                inv.getItemValue(),
                inv.getInvNum(),
                inv.getBrnID(),
                inv.getSupID(),
                inv.getWarntDate(),
                inv.getInvDate(),
                inv.getIssCount(),};
            tbl.addRow(rowData);
        }
    }

