    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed

        try {
            String IDCode = txtMaintainID.getText().trim().substring(0, 3);
            int IDNum = Integer.parseInt(txtMaintainID.getText().trim().substring(3));

            //add search data to the text areas
            if (txtMaintainID.getText().trim().isEmpty()) {

                JOptionPane.showMessageDialog(this, "Please Enter Maintaiance ID");

            } else if (IDNum < 2020) {
                JOptionPane.showMessageDialog(this, "Please Enter Valid ID");

            } else if (IDCode.compareTo("MNT") != 0) {

                JOptionPane.showMessageDialog(this, "Please Enter Valid ID");

            } else {

                String MaintainID = txtMaintainID.getText();

                ArrayList<Maintenance_Issue> IssueDetails = IssuessServiceLayer.SearchIssue_HO(MaintainID);

                for (Maintenance_Issue issue : IssueDetails) {

                    txtMaintainID.setText(issue.getMntID() + "");
                    txtIssueDetails.setText(String.valueOf(issue.getIssDetail()));
                    lblRecordDate.setText(String.valueOf(issue.getIssRecDat()));
                    lblStatus.setText(issue.getStatus());
                    lblInventoryID.setText(String.valueOf(issue.getIntID()));
                    lblBranchID.setText(String.valueOf(issue.getBrnID()));

                    if (issue.getIssFlag().equals("Normal")) {
                        ComboFlag.setSelectedIndex(0);
                    } else {
                        ComboFlag.setSelectedIndex(1);
                    }

                }
            }

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Please Enter Valid ID");
        }

    }//GEN-LAST:event_btnSearchActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // delete searched issue

        if (txtMaintainID.getText().trim().isEmpty()) {

            JOptionPane.showMessageDialog(this, "Please Enter Maintaiance ID");

        } else {

            String MaintainID = txtMaintainID.getText();

            boolean result = IssuessServiceLayer.DeleteIssue(MaintainID);

            if (result) {
                JOptionPane.showMessageDialog(this, "Issue Succesfully Deleted");
            }

        }

    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // update search issue

        if (txtMaintainID.getText().trim().isEmpty()) {

            JOptionPane.showMessageDialog(this, "Please Enter Maintaiance ID");

        } else if (ComboFlag.getSelectedIndex() == -1) {

            JOptionPane.showMessageDialog(this, "Please Select Issue Flag");

        } else if (txtIssueDetails.getText().isEmpty()) {

            JOptionPane.showMessageDialog(this, "Please Enter Issue Details");

        } else {

            String MaintainceID = txtMaintainID.getText();
            int BranID = Integer.parseInt(lblBrnachId.getText());
            String Flag = ComboFlag.getSelectedItem().toString();
            String IssueDetails = txtIssueDetails.getText();
            String RecordDate = lblRecordDate.getText();
            String InventoryID = lblInventoryID.getText();
            String status = lblStatus.getText();

            Maintenance_Issue issue = new Maintenance_Issue(MaintainceID, BranID, InventoryID, "0", " ", Flag, IssueDetails, RecordDate, status, "Not Approved");

            boolean result = IssuessServiceLayer.UpdateIssue(MaintainceID, issue);

            if (result) {
                JOptionPane.showMessageDialog(this, "Issue was updated");
            } else {
                JOptionPane.showMessageDialog(this, "Smothing went wrong..!");
            }
        }
    }//GEN-LAST:event_btnUpdateActionPerformed


