    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed

        int BranchID = Integer.parseInt(txtID.getText());

        ArrayList<Branch> BranchDetails = BranchServiceLayer.SearchBranch(BranchID);

        if (BranchDetails.size() > 0) {

            for (Branch b : BranchDetails) {

                txtAddress.setText(b.getBrnAddr());
                txtTele.setText(String.valueOf(b.getBrnConNum()));
                txtEmail.setText(b.getBrnEmail());
                txtUserName.setText(b.getUserName());
                txtPassword.setText(b.getPassword());
                txtRegionID.setText(String.valueOf(b.getBrnRegionID()));

            }
        } else {
            txtAddress.setText("");
            txtTele.setText("");
            txtEmail.setText("");
            txtUserName.setText("");
            txtPassword.setText("");
            txtRegionID.setText("");
            JOptionPane.showMessageDialog(this, "Data Not Available");

        }


    }//GEN-LAST:event_btnSearchActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed

        int BranchID = Integer.parseInt(txtID.getText());

        boolean result = BranchServiceLayer.DeleteBranch(BranchID);

        if (result) {
            txtAddress.setText("");
            txtTele.setText("");
            txtEmail.setText("");
            txtUserName.setText("");
            txtPassword.setText("");
            txtRegionID.setText("");
            JOptionPane.showMessageDialog(this, "Record Deleted Succesfully");

        } else {
            JOptionPane.showMessageDialog(this, "Data Not Available to Delete");
        }


    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed

        int id = Integer.parseInt(txtID.getText());
        String address = txtAddress.getText();
        int Tele = Integer.parseInt(txtTele.getText());
        String email = txtEmail.getText();
        String userName = txtUserName.getText();
        String Password = txtPassword.getText();
        int ReginID = Integer.parseInt(txtRegionID.getText());

        ArrayList<Branch> BranchDetails = BranchServiceLayer.SearchBranch(id);

        if (BranchDetails.size() > 0) {
            Branch b = new Branch(id, address, Tele, email, userName, Password, ReginID);
            boolean result = BranchServiceLayer.UpdateBranch(id, b);

            if (result) {
                txtAddress.setText("");
                txtTele.setText("");
                txtEmail.setText("");
                txtUserName.setText("");
                txtPassword.setText("");
                txtRegionID.setText("");
                JOptionPane.showMessageDialog(this, "Record Updated Succesfully");

            }

        } else {
            JOptionPane.showMessageDialog(this, "Data Not Available to Update");
        }

    }//GEN-LAST:event_btnUpdateActionPerformed


