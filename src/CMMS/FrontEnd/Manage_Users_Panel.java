    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        String empID = txtEmpID.getText();

        if (txtEmpID.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter a Valid EMP  ID");
        } else if (empID.length() < 4) {
            JOptionPane.showMessageDialog(this, "Please Enter a Valid EMP  ID");
        } else {

            ArrayList<HeadOffice> hoDetails = HeadOfficeServiceLayer.SearchHO(empID);

            if (hoDetails.size() > 0) {

                for (HeadOffice h : hoDetails) {

                    txtEmpEmail.setText(h.getEmpEmail());
                    txtEmpUserName.setText(h.getUserName());
                    txtEmpName.setText(h.getEmpName());
                    txtEmpPass.setText(h.getPassword());

                }
            } else {
                txtEmpEmail.setText("");
                txtEmpUserName.setText("");
                txtEmpName.setText("");
                txtEmpPass.setText("");
                JOptionPane.showMessageDialog(this, "Data Not Available");

            }
        }
    }//GEN-LAST:event_btnSearchActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        String empID = txtEmpID.getText();
        String empEmail = txtEmpEmail.getText();
        String empName = txtEmpName.getText();
        String empUserName = txtEmpUserName.getText();
        String empPassword = txtEmpPass.getText();

        //Null Validation
        if (txtEmpEmail.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter the Email");
        } else if (txtEmpUserName.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter the User Name");
        } else if (txtEmpPass.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter a Password");
        } else if (txtEmpName.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter a Name");
        } //Length Validation
        else if ((empEmail.length() < 5) && (empEmail.length() > 100)) {
            JOptionPane.showMessageDialog(this, "Please Enter a Valid Email");
        } else if ((empUserName.length() < 5) && (empUserName.length() > 25)) {
            JOptionPane.showMessageDialog(this, "Please Enter a Valid UserName");
        } else if (empPassword.length() < 8) {
            JOptionPane.showMessageDialog(this, "Please Enter a Strong Password");
        } else if (empName.length() < 5) {
            JOptionPane.showMessageDialog(this, "Please Enter a Valid Name");
        } //Character Validation
        else {

            ArrayList<HeadOffice> hoDetails = HeadOfficeServiceLayer.SearchHO(empID);

            if (hoDetails.size() > 0) {
                HeadOffice h = new HeadOffice(empUserName, empPassword, empID, empName, empEmail);
                boolean result = HeadOfficeServiceLayer.UpdateHO(empID, h);

                if (result) {
                    txtEmpEmail.setText("");
                    txtEmpUserName.setText("");
                    txtEmpName.setText("");
                    txtEmpPass.setText("");
                    JOptionPane.showMessageDialog(this, "Record Updated Succesfully");

                }

            } else {
                JOptionPane.showMessageDialog(this, "Data Not Available to Update");
            }
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed

        String empID = txtEmpID.getText();

        if (txtEmpID.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter a Valid EMP  ID");
        } else if (empID.length() < 4) {
            JOptionPane.showMessageDialog(this, "Please Enter a Valid EMP  ID");
        } else {

            boolean result = HeadOfficeServiceLayer.DeleteHO(empID);

            if (result) {
                txtEmpEmail.setText("");
                txtEmpUserName.setText("");
                txtEmpName.setText("");
                txtEmpPass.setText("");
                JOptionPane.showMessageDialog(this, "Record Deleted Succesfully");

            } else {
                JOptionPane.showMessageDialog(this, "Data Not Available to Delete");
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed


