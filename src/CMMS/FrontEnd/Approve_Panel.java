    public void GetAllIssues(){
        
        ArrayList<Maintenance_Issue> issueList = ApproveServiceLayer.GetAllIssues();
        
        DefaultTableModel dtm = (DefaultTableModel) tblDetails.getModel();
        
        for (Maintenance_Issue mi : issueList){
            
            Object [] rowData = {
                mi.getMntID(),
                mi.getBrnID(),
                mi.getIntID(),
                mi.getSupID(),
                mi.getMntEmail(),
                mi.getIssFlag(),
                mi.getIssFlag(),
                mi.getIssRecDat(),
                mi.getStatus(),
                mi.getApprovalStat()
                
            };
            
            
            dtm.addRow(rowData);
            
            
        }
        
        
        
    }
    
    
    
    
    
    
    
    

