    //Fetching notes
    public void LoadNotes ()
    {
        ArrayList<note> allnte = NoteServiceLayer.GetUnreadNote(branchID);
        
        DefaultTableModel dtm = (DefaultTableModel) NoteTable.getModel();
        dtm.setRowCount(0);
        
        for ( note nte : allnte ){
            Object[] rowData = {
                nte.getMntID(),
                nte.getSupID(),
                nte.getDetails(),
                nte.getCost()
            };
            
            dtm.addRow(rowData);
        }
    }
