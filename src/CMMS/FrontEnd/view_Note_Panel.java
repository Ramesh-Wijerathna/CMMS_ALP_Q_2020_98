    //Search button
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String serKey = txtMntId.getText();
        if ( serKey == null ) {
            JOptionPane.showMessageDialog(this, "Please provide valid Note ID!", "Error!", 2);
        }
        ArrayList<note> serNote = NoteServiceLayer.SearchNote(serKey, branchID);
        if ( serNote.size() > 0 )
        {
            DefaultTableModel dtm = (DefaultTableModel) NoteTable.getModel();
        dtm.setRowCount(0);
        
        for ( note nte : serNote ){
            Object[] rowData = {
                nte.getMntID(),
                nte.getSupID(),
                nte.getDetails(),
                nte.getCost()
            };
            
            dtm.addRow(rowData);
        }
        } else {
            JOptionPane.showMessageDialog(this, "Invalid Note ID!", "Error!", 2);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    //Marks as read button
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String serKey = txtMntId.getText();
        if ( serKey == null ) {
            JOptionPane.showMessageDialog(this, "Please provide valid Note ID!", "Error!", 2);
        }
        boolean res = NoteServiceLayer.updateViewStatus(serKey);
        if ( res ){
            ArrayList<note> allnte = NoteServiceLayer.GetUnreadNote(branchID);
        
        DefaultTableModel dtm = (DefaultTableModel) NoteTable.getModel();
        dtm.setRowCount(0);
        
        for ( note nte : allnte ){
            Object[] rowData = {
                nte.getMntID(),
                nte.getSupID(),
                nte.getDetails(),
                nte.getCost()
            };
            
            dtm.addRow(rowData);
        }
        } else {
            JOptionPane.showMessageDialog(this, "Invalid Note ID!", "Error!", 2);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

