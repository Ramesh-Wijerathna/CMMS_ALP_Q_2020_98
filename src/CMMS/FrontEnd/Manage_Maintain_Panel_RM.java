    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        try {
            // TODO add your handling code here:
            String mntID = txtMntID.getText();

            String fCode = mntID.trim().substring(0, 3);
            int fNum = Integer.parseInt(mntID.trim().substring(3));

            if (mntID.trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Pleace Enter Maintenance ID");
            } else if (fNum < 2020) {
                JOptionPane.showMessageDialog(this, "Please Enter Valid Maintenance ID");
            } else if (fCode.compareTo("MNT") != 0) {
                JOptionPane.showMessageDialog(this, "Please Enter Valid Maintenance ID");
            } else {

                ArrayList<Approve_List> IssueSet = ManageMaintenanceIssueServiceLayer.SearchMntIssue(mntID);
                for (Approve_List mi : IssueSet) {

                    txtMntID.setText(mi.getMntID() + "");
                    comboFlag.setSelectedItem("");
                    txtIssDetails.setText(mi.getIssDetail());
                    lblIssRecDat.setText(mi.getIssRecDat());
                    lblStatus.setText(mi.getStatus());
                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(Manage_Maintain_Panel_RM.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Manage_Maintain_Panel_RM.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jButton3ActionPerformed


